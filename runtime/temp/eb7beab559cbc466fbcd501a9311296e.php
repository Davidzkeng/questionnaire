	$(".warpper").click(function(event){
		var ev = event || window.event;
        var target = $(ev.target) || $(ev.srcElement);

        if(target.hasClass('option-label')){
        	if (target.prev().children("a").hasClass("jqTransformChecked")) {
        		target.prev().children("a").removeClass("jqTransformChecked");
        		target.next().val("");
        		// target.prev().children("div").children("input").removeAttr("checked");
        		// console.log(target.prev().children("div").children("input"));
        	}else{
        		target.prev().children("a").addClass("jqTransformChecked");
        		target.next().val(target.text());
        		// target.prev().children("div").children("input").attr("checked","checked");
        	}
        	
        	// console.log(target.prev().children("a"));
        }else if (target.hasClass('jqTransformCheckbox')) {
        	if (target.hasClass("jqTransformChecked")) {
        		target.removeClass("jqTransformChecked");
        		target.parent().next().next().val("");
        	}else{
        		target.addClass("jqTransformChecked");
        		target.parent().next().next().val(target.parent().next().text());
        	}
        }else if(target.hasClass('radio-label')){
        	target.prev().children("a").addClass("jqTransformChecked");
        	target.parent().siblings().children("span").children("a").removeClass("jqTransformChecked");
        	target.parent().siblings().children("span").children("div").children("input").removeAttr("checked");
        	target.prev().children("div").children("input").attr("checked","checked");
        	target.parent().parent().children("input").val(target.text());

        }else if (target.hasClass('jqTransformRadio')) {
        	target.addClass("jqTransformChecked");
        	target.parent().parent().siblings().children("span").children("a").removeClass("jqTransformChecked");
        	target.parent().parent().siblings().children("span").children("div").children("input").removeAttr("checked");
        	target.next().children("input").attr("checked","checked");
        	target.parent().parent().parent().children("input").val(target.parent().next().text());
        }
	});
